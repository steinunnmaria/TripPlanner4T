CREATE TABLE HOTELS (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name VARCHAR(64) NOT NULL COLLATE NOCASE,
    region INT NOT NULL,
    town VARCHAR(64) COLLATE NOCASE,
    image VARCHAR(32),
    starRating INT NOT NULL,
    priceRating INT NOT NULL,
    gym BOOLEAN DEFAULT FALSE,
    spa BOOLEAN DEFAULT FALSE,
    wifi BOOLEAN DEFAULT FALSE,
    bar BOOLEAN DEFAULT FALSE,
    restaurant BOOLEAN DEFAULT FALSE
    );

CREATE TABLE ROOMS (
    roomNum INT NOT NULL,
    hotelId INT NOT NULL,
    price INT NOT NULL,
    type INT NOT NULL,
    numBeds INT NOT NULL,
    capacity INT NOT NULL,
    PRIMARY KEY(roomNum,hotelId),
    FOREIGN KEY(hotelId) REFERENCES HOTELS(id)
    );

CREATE TABLE RESERVATIONS (
    reservationId VARCHAR(128) NOT NULL,
    created VARCHAR(10) NOT NULL,
    startDate VARCHAR(10) NOT NULL,
    endDate VARCHAR(10) NOT NULL,
    cName VARCHAR(64) NOT NULL,
    cEmail VARCHAR(64) NOT NULL,
    cPhone VARCHAR(64) NOT NULL,
    numCustomers INT NOT NULL,
    hotelId INT NOT NULL,
    roomNum INT NOT NULL,
    PRIMARY KEY(reservationID),
    FOREIGN KEY(hotelId,roomNum) REFERENCES ROOMS(hotelId,roomNum)
);

